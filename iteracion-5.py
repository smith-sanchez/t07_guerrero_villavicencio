#carlos
#guerrero
#guerrero

import os
#INPUT
msg=os.sys.argv[1].upper()

#BUCLE
for numero in msg:
    if numero == "9":
        print("mi nombre completo es")
    if numero == "8":
        print("roberto")
    if numero == "7":
        print("carlos")
    if numero == "6":
        print("guerrero")
    if numero == "5":
        print("guerrero")
#FIN ITERADOR

print("fin del bucle")
